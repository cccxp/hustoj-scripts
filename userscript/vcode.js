function img2canvas(image) {
    // img元素转为canvas
    let canvas = document.createElement("canvas");
    canvas.width = image.width;
    canvas.height = image.height;
    canvas.getContext("2d").drawImage(image, 0, 0);
    return canvas;
}

function convertToMonoscale(canvas) {
    // 转为二值图像
    let ctx = canvas.getContext("2d");
    let imagedata = ctx.getImageData(0, 0, canvas.width, canvas.height);
    let data = imagedata.data;
    let blackCounter = 0;
    for (let i = 0; i < data.length; i += 4) {
        let grayscale = Math.floor(data[i] * 0.299 + data[i + 1] * 0.587 + data[i + 2] * 0.114);
        if (grayscale < 0x7f) { // 0x7f 为阈值
            data[i] = data[i + 1] = data[i + 2] = 0;
            blackCounter += 1;
        } else {
            data[i] = data[i + 1] = data[i + 2] = 255;
        }
    }
    if (blackCounter < (canvas.width * canvas.height / 2)) {
        // 若黑色像素小于一半则反色一下，保证黑底白字
        for (let i = 0; i < data.length; i += 4) {
            data[i] = 255 - data[i];
            data[i + 1] = 255 - data[i + 1];
            data[i + 2] = 255 - data[i + 2];
        }
    }
    ctx.putImageData(imagedata, 0, 0);
    return canvas;
}


function splitImage(image) {
    // 把验证码中的数字分割出来
    let sx = 5;
    let digits = new Array(4);
    for (let i = 0; i < 4; i++) {
        let newCanvas = document.createElement("canvas");
        let newCtx = newCanvas.getContext("2d");
        newCanvas.width = numsTemplate.width;
        newCanvas.height = numsTemplate.height;
        // 第一个数字左上坐标为5, 5, size为9 * 15， 间距3
        newCtx.drawImage(image, sx, 5, 9, 15, 0, 0, 9, 15);
        digits[i] = newCanvas;
        // console.log(digits[i].toDataURL());
        sx += 12;
    }
    return digits;
}


function match(images) {
    // 匹配
    let vcodeText = "";
    for (let canvas of images) {
        let matched = -1;
        let ctx = canvas.getContext("2d");
        let imagedata = ctx.getImageData(0, 0, canvas.width, canvas.height);
        let data = imagedata.data;
        let arr = new Array();
        for (let i = 0; i < data.length; i += 4) {
            arr.push(data[i] ? 1 : 0);
        }
        let minDiff = 0xffffff;
        for (let num = 0; num < 10; num++) {
            let diff = 0;
            for (let i = 0; i < arr.length; i++) {
                if (arr[i] !== numsTemplate.array[num][i]) {
                    diff++;
                }
            }
            if (diff < minDiff) {
                matched = num;
                minDiff = diff;
            }
        }
        vcodeText += matched.toString();
    }
    return vcodeText;
}

